#include "mymath.h"

inline int fast_abs(int x)
{

	if(x>0) return x; else return (-x);

}

int fast_sqrt(int x)
{
	int l=0,r=0;
	l=x;
	while(fast_abs(l-r)>1){
		r=x/l;
		l=(l+r)/2;
	}
	return(l);
}



